import pandas as pd

# 1. Создаем DataFrame с данными
data = {
    'Ученик': ['Алексей', 'Мария', 'Иван', 'Елена', 'Павел', 'Светлана', 'Дмитрий', 'Ольга', 'Сергей', 'Татьяна'],
    'Математика': [4, 5, 3, 4, 5, 2, 4, 5, 3, 4],
    'Русский': [5, 4, 4, 3, 5, 3, 4, 4, 2, 5],
    'Физика': [3, 4, 2, 3, 4, 2, 3, 4, 2, 3],
    'История': [4, 4, 3, 4, 5, 3, 4, 4, 3, 4],
    'Информатика': [5, 5, 4, 4, 5, 3, 4, 5, 4, 5]
}

df = pd.DataFrame(data)

# 2. Вывод первых нескольких строк для проверки
print("Первые строки DataFrame:")
print(df.head())

# 3. Средняя оценка по каждому предмету
mean_scores = df[['Математика', 'Русский', 'Физика', 'История', 'Информатика']].mean()
print("\nСредние оценки по предметам:")
print(mean_scores)

# 4. Медианная оценка по каждому предмету
median_scores = df[['Математика', 'Русский', 'Физика', 'История', 'Информатика']].median()
print("\nМедианные оценки по предметам:")
print(median_scores)

# 5. Q1 и Q3 для оценки по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nКвартиль 1 (Q1) по Математике: {Q1_math}")
print(f"Квартиль 3 (Q3) по Математике: {Q3_math}")
print(f"Интерквартильный размах (IQR) по Математике: {IQR_math}")

# 6. Стандартное отклонение по каждому предмету
std_devs = df[['Математика', 'Русский', 'Физика', 'История', 'Информатика']].std()
print("\nСтандартные отклонения по предметам:")
print(std_devs)